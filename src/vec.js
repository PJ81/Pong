export default class Vec {
  constructor(x = 0, y = 0) {
    this.x = x;
    this.y = y;
  }

  set(x, y) {
    this.x = x;
    this.y = y;
  }

  get len() {
    return Math.sqrt(this.x * this.x + this.y * this.y);
  }

  set len(val) {
    const fact = val / this.len;
    this.x *= fact;
    this.y *= fact;
  }
}